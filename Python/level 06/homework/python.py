num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
num3 = int(input("Enter the third number: "))

# Addition
sum_numbers = num1 + num2 + num3

# Multiplication
product = num1 * num2 * num3

# Subtraction
difference = num1 - num2 - num3

# Print the results
print(f"The sum of the entered numbers is: {sum_numbers}")
print(f"The product of the entered numbers is: {product}")
print(f"The result of subtracting the second and third numbers from the first is: {difference}")
